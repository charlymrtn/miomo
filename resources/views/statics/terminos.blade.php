	<h2>ASPECTOS LEGALES</h2>

	<h3>REQUISITOS PARA LA OPERACIÓN DE UN SITIO DE APUESTAS ONLINE</h3>
	<p>
		En el Reglamento de la Ley de Juegos y Sorteos, se estipulan algunos aspectos que debe contener el portal, los cuales se mencionarán enseguida.
	</p>
	<ul>
		<li>Las apuestas deben contener fecha, hora y resultado de la misma.</li>
		<li>Los resultados de las apuestas deben estar disponibles en el portal, a manera de que los apostadores y books que participaron puedan verlos.</li>
		<li>Los resultados de las apuestas deben estar disponibles mínimo 90 días hábiles después de efectuarse el evento. </li>
		<li>Sí el evento del cual se realizarán las apuestas es suspendido, el permisionario tiene la obligación de regresar a cada uno de los participantes el monto que depositó por las apuestas.</li>
		<li>En el portal donde se llevan a cabo el cruce de apuestas, se deberá contar con algún sistema de control interno para las transacciones, dicho control debe contener al menos la siguiente información:
			<ol>
				<li>Número de la cuenta e identidad del apostador.</li>
				<li>Fecha, hora, número de transacción y cantidad apostada.  </li>
			</ol>

		</li>

	</ul>

<P>Otro de los temas importantes a la hora de cruzar apuestas vía internet, es el comprobante de la apuesta realizada, para ello el Reglamento de la Ley de Juegos y Sorteos exige se incluyan algunos elementos en dichos comprobantes.
</P>

<ul>
	<li>Por cada apuesta aceptada, el operador debe emitir un comprobante, el cual deberá contener un número de serie, fecha, hora de expedición, monto de la apuesta y tipo de apuesta. Esta información quedará registrada en el sistema central de las apuestas inmediatamente después de ser pagada la apuesta.  </li>
	<li>Los apostadores y books que participen en las apuestas, deberán tener acceso a su comprobante, el cual, debe ser imprimible, así mismo, podrán consultar los derechos que les correspondan.</li>
	<li>Para cualquier duda o aclaración respecto a los pagos por las apuestas, el operador tiene la obligación de guardar por lo menos 90 días hábiles los registros de ganador.</li>
</ul>

<P>Por otra parte, entre la información requerida por el Reglamento de la Ley de Juegos y Sorteos, al operador del sitio se le pide incluir en el portal lo siguiente:</P>

<ul>
	<li>Detalle de la política de juego responsable del operador y su compromiso con la misma.</li>
	<li>Una declaración de advertencia de que el juego podría ser perjudicial  si no se controla. </li>
	<li>Consejos sobre el juego responsable y un enlace a fuentes de ayuda en problemas de juego, incluidos los números de línea de ayuda. </li>
	<li>Un proceso de autoevaluación  aceptada  y simple para determinar el potencial de riesgo del apostador o book. </li>
	<li>La comunicación directa con el jugador y el book deberá llevar un mensaje de juego responsable, siempre que sea posible. </li>
	<li>Un reloj deberá estar disponible en la pantalla todo el tiempo. </li>
	<li>La página de inicio del sitio web, deberá mostrar claramente la indicación, a manera de mensaje o signo, que personas menores a 18 años no están permitidas para participar en dichos juegos de azar. </li>
	<li>En el formulario de registro, se le pedirá al apostador o book proporcionar la siguiente información, la cual, servirá para efectuar su registro y participar de nuestros juegos y servicios: nombre, fecha de nacimiento, dirección, nombre de usuario y contraseña.  </li>
	<li>Asegurarle al apostador o book que los depósitos y retiros efectuados en MIOMO se reflejan de forma inmediata en su cuenta. </li>
	<li>Demostrar que hay una protección de los fondos del jugador.</li>
</ul>
